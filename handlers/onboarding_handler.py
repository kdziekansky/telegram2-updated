async def handle_onboarding_step(update: Update, context: ContextTypes.DEFAULT_TYPE, next_step=None):
    """Obs≈Çuguje przej≈õcie do nastƒôpnego kroku onboardingu"""
    query = update.callback_query
    user_id = query.from_user.id
    language = get_user_language(context, user_id)
    
    # Pobierz aktualny krok lub ustaw pierwszy, je≈õli nie istnieje
    if 'user_data' not in context.chat_data:
        context.chat_data['user_data'] = {}
    
    if user_id not in context.chat_data['user_data']:
        context.chat_data['user_data'][user_id] = {}
    
    if 'onboarding_step' not in context.chat_data['user_data'][user_id]:
        context.chat_data['user_data'][user_id]['onboarding_step'] = 0
        context.chat_data['user_data'][user_id]['onboarding_tasks_completed'] = []
    
    # Pobierz aktualny krok lub u≈ºyj podanego
    if next_step is not None:
        current_step = next_step
    else:
        current_step = context.chat_data['user_data'][user_id]['onboarding_step']
    
    # Sprawd≈∫, czy to nie jest ostatni krok
    if current_step >= len(ONBOARDING_STEPS):
        current_step = len(ONBOARDING_STEPS) - 1
    
    # Zapisz aktualny krok
    context.chat_data['user_data'][user_id]['onboarding_step'] = current_step
    
    # Pobierz dane kroku
    step = ONBOARDING_STEPS[current_step]
    
    # Sprawd≈∫ czy zadanie zosta≈Ço wykonane
    tasks_completed = context.chat_data['user_data'][user_id].get('onboarding_tasks_completed', [])
    is_task_completed = step['id'] in tasks_completed
    
    # Przygotuj tekst
    text = f"üëã *{step['title']}*\n\n{step['description']}\n\n"
    
    # Dodaj oznaczenie uko≈Ñczonego zadania lub opis zadania
    if is_task_completed:
        text += f"‚úÖ *Zadanie uko≈Ñczone!*\n\n"
    elif step['has_task']:
        text += f"üéØ *{step['task']}*\n\n"
    else:
        text += f"{step['task']}\n\n"
    
    # Dodaj wska≈∫nik postƒôpu
    progress = f"{current_step + 1}/{len(ONBOARDING_STEPS)}"
    text += f"üìä *Postƒôp: {progress}*"
    
    # Przygotuj przyciski
    keyboard = []
    row = []
    
    # Przycisk wstecz dla wszystkich krok√≥w opr√≥cz pierwszego
    if current_step > 0:
        row.append(
            InlineKeyboardButton("‚¨ÖÔ∏è " + get_text("onboarding_back", language, default="Wstecz"), 
                                callback_data="onboarding_back")
        )
    
    # Przycisk dalej/zako≈Ñcz
    if current_step < len(ONBOARDING_STEPS) - 1:
        # Je≈õli zadanie musi byƒá wykonane i nie jest uko≈Ñczone, przycisk jest nieaktywny
        if step['has_task'] and not is_task_completed:
            button_text = "‚û°Ô∏è " + get_text("onboarding_complete_task", language, default="Wykonaj zadanie")
        else:
            button_text = "‚û°Ô∏è " + get_text("onboarding_next", language, default="Dalej")
        
        row.append(InlineKeyboardButton(button_text, callback_data="onboarding_next"))
    else:
        # Ostatni krok - przycisk "Zako≈Ñcz"
        row.append(
            InlineKeyboardButton("üèÅ " + get_text("onboarding_finish", language, default="Zako≈Ñcz"), 
                                callback_data="onboarding_finish")
        )
    
    keyboard.append(row)
    
    # Dodaj przycisk do pominiƒôcia ca≈Çego onboardingu
    keyboard.append([
        InlineKeyboardButton(get_text("onboarding_skip", language, default="Pomi≈Ñ przewodnik"), 
                            callback_data="onboarding_skip")
    ])
    
    reply_markup = InlineKeyboardMarkup(keyboard)
    
    # Aktualizuj wiadomo≈õƒá
    try:
        await query.edit_message_caption(
            caption=text,
            reply_markup=reply_markup,
            parse_mode=ParseMode.MARKDOWN
        )
    except Exception as e:
        print(f"B≈ÇƒÖd przy aktualizacji wiadomo≈õci onboardingu: {e}")
        # Spr√≥buj wys≈Çaƒá nowƒÖ wiadomo≈õƒá, je≈õli aktualizacja siƒô nie powiedzie
        try:
            await query.delete_message()
            await context.bot.send_photo(
                chat_id=query.message.chat_id,
                photo=step['image_url'],
                caption=text,
                reply_markup=reply_markup,
                parse_mode=ParseMode.MARKDOWN
            )
        except Exception as e2:
            print(f"Drugi b≈ÇƒÖd przy wysy≈Çaniu wiadomo≈õci onboardingu: {e2}")

async def handle_onboarding_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Obs≈Çuguje callbacki zwiƒÖzane z onboardingiem"""
    query = update.callback_query
    user_id = query.from_user.id
    language = get_user_language(context, user_id)
    
    await query.answer()
    
    # Sprawd≈∫, czy u≈ºytkownik jest w trybie onboardingu
    if ('user_data' not in context.chat_data or 
        user_id not in context.chat_data['user_data'] or 
        'onboarding_active' not in context.chat_data['user_data'][user_id] or 
        not context.chat_data['user_data'][user_id]['onboarding_active']):
        return False
    
    # Obs≈Çuga przycisk√≥w onboardingu
    if query.data == "onboarding_next":
        # Pobierz aktualny krok
        current_step = context.chat_data['user_data'][user_id]['onboarding_step']
        step = ONBOARDING_STEPS[current_step]
        
        # Sprawd≈∫, czy to krok z zadaniem, kt√≥re musi byƒá wykonane
        if step['has_task']:
            # Sprawd≈∫, czy zadanie zosta≈Ço wykonane
            tasks_completed = context.chat_data['user_data'][user_id].get('onboarding_tasks_completed', [])
            if step['id'] not in tasks_completed:
                # Zadanie nie zosta≈Ço wykonane, poka≈º komunikat
                await query.answer(get_text("onboarding_task_required", language, 
                                           default="Najpierw wykonaj zadanie."))
                return True
        
        # Przejd≈∫ do nastƒôpnego kroku
        next_step = min(current_step + 1, len(ONBOARDING_STEPS) - 1)
        await handle_onboarding_step(update, context, next_step)
        return True
    
    elif query.data == "onboarding_back":
        # Przejd≈∫ do poprzedniego kroku
        current_step = context.chat_data['user_data'][user_id]['onboarding_step']
        prev_step = max(0, current_step - 1)
        await handle_onboarding_step(update, context, prev_step)
        return True
    
    elif query.data == "onboarding_skip" or query.data == "onboarding_finish":
        # Zako≈Ñcz onboarding
        context.chat_data['user_data'][user_id]['onboarding_active'] = False
        
        # Wy≈õlij podsumowanie po zako≈Ñczeniu onboardingu
        completed_tasks = len(context.chat_data['user_data'][user_id].get('onboarding_tasks_completed', []))
        total_tasks = sum(1 for step in ONBOARDING_STEPS if step['has_task'])
        
        # Usu≈Ñ wiadomo≈õƒá onboardingu
        try:
            await query.message.delete()
        except:
            pass
        
        # Wy≈õlij podsumowanie
        summary = f"üéâ *{get_text('onboarding_completed', language, default='Przewodnik uko≈Ñczony!')}*\n\n"
        summary += f"{get_text('onboarding_tasks_completed', language, default='Uko≈Ñczone zadania')}: "
        summary += f"*{completed_tasks}/{total_tasks}*\n\n"
        
        if completed_tasks < total_tasks:
            summary += f"{get_text('onboarding_remaining_tasks', language, default='Mo≈ºesz w dowolnej chwili wypr√≥bowaƒá pozosta≈Çe funkcje. U≈ºyj /help, aby zobaczyƒá dostƒôpne komendy.')}\n\n"
        
        summary += f"{get_text('onboarding_success', language, default='Dziƒôkujemy za przej≈õcie przewodnika! Teraz mo≈ºesz w pe≈Çni korzystaƒá z bota.')}"
        
        # Dodaj przycisk do menu g≈Ç√≥wnego
        keyboard = [[InlineKeyboardButton("üè† " + get_text("main_menu", language, default="Menu g≈Ç√≥wne"), 
                                         callback_data="menu_back_main")]]
        reply_markup = InlineKeyboardMarkup(keyboard)
        
        await context.bot.send_message(
            chat_id=query.message.chat_id,
            text=summary,
            reply_markup=reply_markup,
            parse_mode=ParseMode.MARKDOWN
        )
        
        # Dodaj bonus kredytowy za uko≈Ñczenie przynajmniej po≈Çowy zada≈Ñ
        if completed_tasks >= total_tasks / 2:
            from database.credits_client import add_user_credits
            bonus_credits = 10  # 10 kredyt√≥w jako bonus
            add_user_credits(user_id, bonus_credits, "Bonus za uko≈Ñczenie przewodnika")
            
            # Powiadom u≈ºytkownika o bonusie
            bonus_message = f"üéÅ *{get_text('onboarding_bonus', language, default='Bonus!')}*\n\n"
            bonus_message += f"{get_text('onboarding_bonus_credits', language, default='Otrzymujesz')} *{bonus_credits}* "
            bonus_message += f"{get_text('credits', language, default='kredyt√≥w')} "
            bonus_message += f"{get_text('onboarding_bonus_reason', language, default='za uko≈Ñczenie przewodnika')}!"
            
            await context.bot.send_message(
                chat_id=query.message.chat_id,
                text=bonus_message,
                parse_mode=ParseMode.MARKDOWN
            )
        
        return True
    
    return False

def check_onboarding_task(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """
    Sprawdza, czy wiadomo≈õƒá u≈ºytkownika jest wykonaniem zadania onboardingu
    
    Args:
        update: Obiekt Update
        context: Kontekst bota
    
    Returns:
        bool: True je≈õli zadanie zosta≈Ço wykonane, False w przeciwnym razie
    """
    # Sprawd≈∫, czy u≈ºytkownik jest w trybie onboardingu
    user_id = update.effective_user.id
    
    if ('user_data' not in context.chat_data or 
        user_id not in context.chat_data['user_data'] or 
        'onboarding_active' not in context.chat_data['user_data'][user_id] or 
        not context.chat_data['user_data'][user_id]['onboarding_active']):
        return False
    
    # Pobierz aktualny krok
    current_step = context.chat_data['user_data'][user_id]['onboarding_step']
    step = ONBOARDING_STEPS[current_step]
    
    # Sprawd≈∫, czy to krok z zadaniem
    if not step['has_task']:
        return False
    
    # Sprawd≈∫, czy zadanie zosta≈Ço ju≈º wykonane
    tasks_completed = context.chat_data['user_data'][user_id].get('onboarding_tasks_completed', [])
    if step['id'] in tasks_completed:
        return False
    
    # Sprawd≈∫, czy wiadomo≈õƒá to wykonanie zadania
    if 'task_command' in step:
        # Zadanie wymaga u≈ºycia komendy
        if update.message and update.message.text and update.message.text.startswith(step['task_command']):
            # Zadanie wykonane
            if 'onboarding_tasks_completed' not in context.chat_data['user_data'][user_id]:
                context.chat_data['user_data'][user_id]['onboarding_tasks_completed'] = []
            
            context.chat_data['user_data'][user_id]['onboarding_tasks_completed'].append(step['id'])
            return True
    
    elif 'task_keyword' in step:
        # Zadanie wymaga u≈ºycia s≈Ç√≥w kluczowych
        if update.message and update.message.text:
            text = update.message.text.lower()
            # Sprawd≈∫, czy tekst zawiera s≈Çowa kluczowe
            if any(keyword in text for keyword in step['task_keyword']):
                # Zadanie wykonane
                if 'onboarding_tasks_completed' not in context.chat_data['user_data'][user_id]:
                    context.chat_data['user_data'][user_id]['onboarding_tasks_completed'] = []
                
                context.chat_data['user_data'][user_id]['onboarding_tasks_completed'].append(step['id'])
                return True
    
    return False

